    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
        String kullaniciAdi=kullaniciAdiAlani.getText();
        String sifre=sifreAlani.getText();
        String telefon=telefonAlani.getText();
        String adres=adresAlani.getText();
        String bakiye=bakiyeAlani.getText();
        String eposta=epostaAlani.getText();
        String uyeTuru=uyeTuruAlani.getText();
        String uyeID=uyeIDAlani.getText();
        int selectedrow=uyeTablosu.getSelectedRow();
        if(selectedrow==-1){
            if(model.getRowCount()==0){
                mesajYazisi.setText("HIC BIR UYE BULUNMUYOR!!!");
                
            }
            else{
                mesajYazisi.setText("Guncellemek istedigniz uyeyi seciniz.");
            }
        }
        else {
            int id=(int)model.getValueAt(selectedrow, 0);
            islemler.uyeGuncelle(id,kullaniciAdi,sifre,telefon,adres,bakiye,eposta);
            uyeGoruntule();
            mesajYazisi.setText("UYE BASARIYLA GUNCELLENDI");
        }
    }//GEN-LAST:event_guncelleActionPerformed

