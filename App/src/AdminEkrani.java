    public void uyeGoruntule(){ //bu fonksiyonu yazdim
        model.setRowCount(0);// uyeTablosu sifirlaniyor.
        ArrayList<Uye> uyeler=new ArrayList<Uye>();
        
       uyeler= islemler.uyeleriGetir();
       if(uyeler!=null){
           for(Uye uye:uyeler){
               Object[] eklenecek={uye.getUyeID(),uye.getKullaniciAdi(),uye.getSifre(),uye.getTelefon(),uye.getAdres(),uye.getBakiye(),uye.getEposta(),uye.getUyeTuru()};
               model.addRow(eklenecek);
           }
       }
    }
    public void duyuruGoruntule(){
        ArrayList<Duyurular> duyurular=new ArrayList<Duyurular>();
        duyurularAlani.setText("");
        
        duyurular=islemler.duyurulariGetir();
        if(duyurular!=null){
            for(Duyurular duyuru:duyurular){
                duyurularAlani.setText(duyurularAlani.getText()+duyuru.getDuyuruID()+"   "+duyuru.getBaslik()+"   "+duyuru.getAciklama()+"\n");
            }
        }
    }
