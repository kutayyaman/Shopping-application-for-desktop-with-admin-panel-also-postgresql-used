    private void girisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_girisActionPerformed
        mesaj_yazisi.setText("");
        String kullanici_adi=kullanici_adi_alani.getText();
        String parola=new String(parola_alani.getPassword());
        
        boolean girisBasariliMi=girisKontrol.girisYap(kullanici_adi, parola);
        boolean adminMi=girisKontrol.adminKontrolu(kullanici_adi,parola);
        if(girisBasariliMi==true && adminMi==true){
            
            AdminEkrani adminEkrani=new AdminEkrani(this,true,kullanici_adi,parola);
            setVisible(false);
            adminEkrani.setVisible(true);
            
        }
        else if(girisBasariliMi==true){
            KullaniciEkrani kullaniciEkrani=new KullaniciEkrani(this,true,kullanici_adi,parola);
            setVisible(false);
            kullaniciEkrani.setVisible(true);
            addWindowListener(this);
        }
        else{
            mesaj_yazisi.setText("Giris Basarisiz... Tekrar Deneyiniz");
        }
    }//GEN-LAST:event_girisActionPerformed

