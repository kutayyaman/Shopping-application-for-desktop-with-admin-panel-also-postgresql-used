    private void uyeOlmaButonuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uyeOlmaButonuActionPerformed
        String kullaniciAdi=yeniUyeKullaniciAdi.getText();
        String sifre=yeniUyeParola.getText();
        String ePosta=yeniUyeEposta.getText();
        String adres=yeniUyeAdres.getText();
        String telefonNo=yeniUyeTelefon.getText();
        if(kullaniciAdi.equals("")|| sifre.equals("")|| ePosta.equals("")|| adres.equals("")|| telefonNo.equals("")){
            mesaj_yazisi.setText("Bilgilerinizi Girin.");
            return;
        }
        boolean uyeOlabilirMi=girisKontrol.uyeYap(kullaniciAdi,sifre,ePosta,adres,telefonNo);
        if(!uyeOlabilirMi){
            mesaj_yazisi.setText("kullanici adi kullaniliyor");
            return;
        }
        mesaj_yazisi.setText("Basariyla Uye Oldunuz 100 TL hediye kazandiniz.");
    }//GEN-LAST:event_uyeOlmaButonuActionPerformed

