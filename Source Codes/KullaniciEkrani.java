    private void buttonSatinAlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSatinAlActionPerformed
        mesajYazisi.setText("");
        
        int selectedrow=urunlerTablosu.getSelectedRow();
        if(selectedrow==-1){
            if(model.getRowCount()==0){
                mesajYazisi.setText("Urunlerimiz Bitmistir");
            }
            else{
                mesajYazisi.setText("Lutfen Urun Seciniz");
            }
        }
        else{
            int id=(int)model.getValueAt(selectedrow,0);
            boolean bakiyeYeterliMi=islemler.urunuSatinAl(id,kullaniciAdi); //bakiye yeterli ve stokta varsa islemleri yapip true donecek.
            if(!bakiyeYeterliMi)
                mesajYazisi.setText("Bakiyenizi Ve Urun Stogunu Kontrol Ediniz.");
            else{
                urunGoruntule();
                kullaniciGoruntule(kullaniciAdi,sifre);
                mesajYazisi.setText("Urunu Basariyla Satin Aldiniz.");
            }
        }
    }//GEN-LAST:event_buttonSatinAlActionPerformed

    
    
    
    
