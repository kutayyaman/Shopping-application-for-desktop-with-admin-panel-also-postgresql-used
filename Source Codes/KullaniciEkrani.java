    public void kullaniciGoruntule(String kullaniciAdi,String sifre){
        Uye girisYapanUye=islemler.girisYapanUyeBilgileriGetir(kullaniciAdi,sifre);
        if(girisYapanUye!=null){
            labelKullaniciAdi.setText("Kullanici Adiniz: "+girisYapanUye.getKullaniciAdi());
            labelEposta.setText("Eposta Adresiniz: "+girisYapanUye.getEposta());
            labelBakiye.setText("Bakiyeniz: "+girisYapanUye.getBakiye()+" TL");
        }
    }
    public void duyuruGoruntule(){
        ArrayList<Duyurular> duyurular=new ArrayList<Duyurular>();
        duyurularAlani.setText("DUYURULAR\n");
        
        duyurular=islemler.duyurulariGetir();
        if(duyurular!=null){
            for(Duyurular duyuru:duyurular){
                duyurularAlani.setText(duyurularAlani.getText()+duyuru.getDuyuruID()+"   "+duyuru.getBaslik()+"   "+duyuru.getAciklama()+"\n");
            }
        }
    }
    
    public void urunGoruntule(){
        model.setRowCount(0);
        ArrayList<Urun> urunler=new ArrayList<Urun>();
        urunler=islemler.urunleriGetir();
        if(urunler!=null){
            for(Urun urun:urunler){
                Object[] eklenecek={urun.getUrunID(),urun.getUrunAdi(),urun.getAciklama(),urun.getStok(),urun.getFiyati()};
                model.addRow(eklenecek);
            }
        }
    }

    
    
